class bitwise2// class name
{
    public static void main(String args[]){//main methods
        int a =10;// integer variable a with value 10
        int b =5;// integer variable b with value 5
        int c =20;// integer variable c with value 20
        System.out.println(a<b&&a++<c);//result for bitwise and operation with expression
        System.out.println(a);//result for variable a
        System.out.println(a<b&a++<c);//result for bitwise and operation with expression
        System.out.println(a);// result for variable a

    }//end of class
}//end of main methods
    

