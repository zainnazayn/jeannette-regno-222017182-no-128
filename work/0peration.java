 class OperatorExample{  // class name
    public static void main(String args[]){ //main methods 
        //For positive number, >> and >>> works same  
        System.out.println(20>>2); // positive right shift operation  
        System.out.println(20>>>2);  //positive right shift operation
        //For negative number, >>> changes parity bit (MSB) to 0  
        System.out.println(-20>>2); // negative right shift operation 
        System.out.println(-20>>>2); //negative right shift operation 
    }} // end of class name and main methods 
    

